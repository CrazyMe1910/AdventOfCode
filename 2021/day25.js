function day25(input) {
    input = input.split('\n').map(str => str.split(''));

    let steps = 0;
    while (steps < Infinity) {
        steps++;

        let moveRight = [];
        let moveDown = [];

        for (let i = 0; i < input.length; i++) {
            for (let j = 0; j < input[i].length; j++) {
                if (input[i][j] == '>') {
                    if (j == (input[i].length - 1)) {
                        if (input[i][0] == '.') {
                            moveRight.push([i, j]);
                        }
                    } else if (input[i][j + 1] == '.') {
                        moveRight.push([i, j]);
                    }
                }
            }
        }

        if (moveRight.length != 0) {
            for (let k = 0; k < moveRight.length; k++) {
                if (moveRight[k][1] == (input[moveRight[k][0]].length - 1)) {
                    input[moveRight[k][0]][moveRight[k][1]] = '.';
                    input[moveRight[k][0]][0] = '>';
                } else {
                    input[moveRight[k][0]][moveRight[k][1]] = '.';
                    input[moveRight[k][0]][moveRight[k][1] + 1] = '>';
                }
            }
        }

        for (let i = 0; i < input.length; i++) {
            for (let j = 0; j < input[i].length; j++) {
                if (input[i][j] == 'v') {
                    if (i == (input.length - 1)) {
                        if (input[0][j] == '.') {
                            moveDown.push([i, j]);
                        }
                    } else if (input[i + 1][j] == '.') {
                        moveDown.push([i, j]);
                    }
                }
            }
        }

        if (moveDown.length != 0) {
            for (let l = 0; l < moveDown.length; l++) {
                if (moveDown[l][0] == (input.length - 1)) {
                    input[moveDown[l][0]][moveDown[l][1]] = '.';
                    input[0][moveDown[l][1]] = 'v';
                } else {
                    input[moveDown[l][0]][moveDown[l][1]] = '.';
                    input[moveDown[l][0] + 1][moveDown[l][1]] = 'v';
                }
            }
        }
        
        if (moveRight.length == 0 && moveDown.length == 0) {
            return steps;
        }
    }
}

