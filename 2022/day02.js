function day02(input, partTwo) {
    input = input.split('\n').map(str => str.split(' '));

    const choiceScores = {'X': 1, 'Y': 2, 'Z': 3};
    const endingScores = {'X': 0, 'Y': 3, 'Z': 6};
    let totalScore = 0;

    for (let i = 0; i < input.length; i++) {
        const elfChoice = input[i][0];
        const myChoice = input[i][1];

        
        if (partTwo) {
            totalScore += endingScores[myChoice];

            switch (myChoice) {
                case 'X':
                    if (elfChoice == 'A') {
                        totalScore += choiceScores['Z'];
                    } else if (elfChoice == 'B') {
                        totalScore += choiceScores['X'];
                    } else {
                        totalScore += choiceScores['Y'];
                    }
                    break;
                case 'Y':
                    if (elfChoice == 'C') {
                        totalScore += choiceScores['Z'];
                    } else if (elfChoice == 'A') {
                        totalScore += choiceScores['X'];
                    } else {
                        totalScore += choiceScores['Y'];
                    }
                    break;
                case 'Z':
                    if (elfChoice == 'B') {
                        totalScore += choiceScores['Z'];
                    } else if (elfChoice == 'C') {
                        totalScore += choiceScores['X'];
                    } else {
                        totalScore += choiceScores['Y'];
                    }
                    break;
                default:
                    break;
            }
            continue;
        }
        
        totalScore += choiceScores[myChoice];
        switch (elfChoice) {
            case 'A':
                if (myChoice == 'X') { 
                    totalScore += 3;
                } else if (myChoice == 'Y') {
                    totalScore += 6;
                }
                break;
            case 'B':
                if (myChoice == 'Y') {
                    totalScore += 3;
                } else if (myChoice == 'Z') {
                    totalScore += 6;
                }
                break;
            case 'C':
                if (myChoice == 'Z') {
                    totalScore += 3;
                } else if (myChoice == 'X') {
                    totalScore += 6;
                }
                break;
            default:
                break;
        }
    }
    
    return totalScore;
}

