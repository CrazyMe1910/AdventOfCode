function day03() {
    let encounterArr = [];
    const steps = [
        [1, 1],
        [3, 1],
        [5, 1],
        [7, 1],
        [1, 2]
    ];
    for (let k = 0; k < steps.length; k++) {
        const right = steps[k][0];
        const down = steps[k][1];

        let encounteredTrees = 0;

        let i = 0
        let j = 0;
        while (i < day03Arr.length) {
            if (day03Arr[i][j] == "#") {
                encounteredTrees++;
            }

            // array has infinite copies to the right, so j has to start over if it reaches near the end of the array
            if ((j + right) > (day03Arr[i].length - 1)) {
                j = ((j + right) - (day03Arr[i].length));
            } else {
                j += right;
            }

            i += down;
        }
        encounterArr.push(encounteredTrees);
    }

    const answerOne = encounterArr[1];
    const answerTwo = encounterArr.reduce((a, b) => a * b, 1);

    return [answerOne, answerTwo];
}

